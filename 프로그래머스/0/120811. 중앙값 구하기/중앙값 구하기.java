import java.util.*;

class Solution {
    public int solution(int[] array) {
        int[] newArr = array;
        
        for (int i = 0; i < array.length - 1; i++) {
            for (int j = 0; j < array.length - 1 - i; j++) {
                if (array[j] > array[j + 1]) {
                    int tmp = array[j];
                    array[j] = array[j + 1];
                    array[j + 1] = tmp;
                }
            }
        }
        return newArr[array.length/2];
    }
}